async function sendToken(token_contract_address, recAddress, amount){
    return new Promise(async (resolve, reject)=>{
        const token_contract = new web3.eth.Contract(token_abi, token_contract_address);
        recAddress = await web3.utils.toChecksumAddress(recAddress)
        let gaslimit = await token_contract.methods.transfer(recAddress, `${amount*10**18}`).estimateGas({from:add});
        var transfer = token_contract.methods.transfer(recAddress, `${amount*10**18}`)
        var encodedABI = transfer.encodeABI();

        var tx = {
            from: add,  // Owner address
            to: recAddress, //conttract address
            gas: gaslimit,
            data: encodedABI
        };

        web3.eth.accounts.signTransaction(tx, privateKey).then(signed => {
            var tran = web3.eth.sendSignedTransaction(signed.rawTransaction);
            tran.on('receipt', receipt => {
                console.log('reciept',receipt);
                const tx = receipt.transactionHash;
                resolve(tx)

            });
            tran.on('error', error => {
                resolve(error)
            });
        });
    })
    
}

async function sentNFT(nft_contract_address,recAddress,tokenid){
    return new Promise(async (resolve, reject)=>{
        const nft_contract = new web3.eth.Contract(nft_abi, nft_contract_address);
        recAddress = await web3.utils.toChecksumAddress(recAddress)
        let gaslimit = await nft_contract.methods.transferFrom(add, recAddress, tokenid).estimateGas({from:add});
        var transfer = nft_contract.methods.transferFrom(add, recAddress, tokenid)
        var encodedABI = transfer.encodeABI();

        var tx = {
            from: add,  // Owner address
            to: recAddress, //conttract address
            gas: gaslimit,
            data: encodedABI
        };

        web3.eth.accounts.signTransaction(tx, privateKey).then(signed => {
            var tran = web3.eth.sendSignedTransaction(signed.rawTransaction);
            tran.on('receipt', receipt => {
                console.log('reciept',receipt);
                const tx = receipt.transactionHash;
                resolve(tx)

            });
            tran.on('error', console.error);
        });
    })
}

